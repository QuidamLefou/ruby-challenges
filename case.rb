current_weather = 'snowy'

case current_weather
when 'Sunny'
	puts "It's sunny! Go outside and enjoy this beautiful day!"
when 'rainy'
	puts "Ick rain. Don't forget your umbrella"
when 'cloudy'
	puts "Well there's no sun but that doesn't mean you can't still enjoy the day."
when 'foggy'
	puts "A foggy day, spooky!"
when 'snowy'
	puts "With all this snow it's best to stay in and make hot chocolate."
when 'windy'
	puts "hold on to your hats it's a windy day"
end
